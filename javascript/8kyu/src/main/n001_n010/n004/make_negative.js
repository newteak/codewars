/**
* makeNegative return given a number to make it negative.
* URL: https://www.codewars.com/kata/55685cd7ad70877c23000102/train/javascript
*
*
* @param {Number} num - any number.
*
* @return {Number} given a number to make it negative.
*
*/
const makeNegative = (num) => num < 0 ? num : -num;

export default makeNegative;
