/**
* repeatStr repeats the given string exactly n times.
* URL: https://www.codewars.com/kata/57a0e5c372292dd76d000d7e/train/javascript
*
*
* @param {Number} n - number which is greater than 0.
* @param {String} s - string that you want to repeat.
*
* @return {String} prepeats the given string exactly n times.
*
*/
const repeatStr = (n, s) => s.repeat(n);

export default repeatStr;
