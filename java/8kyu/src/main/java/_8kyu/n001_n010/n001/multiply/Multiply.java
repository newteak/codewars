package _8kyu.n001_n010.n001.multiply;

public class Multiply {
    /**
     * multiply method multiply two int param
     *
     * @param firstParam  first param that will be multiplied
     * @param secondParam second param that will be multiplied
     * @return multiplied value
     */
    public static int multiply(int firstParam, int secondParam) {
        return firstParam * secondParam;
    }
}
